    val tipo: String,
    val kcal: Int,
    val proteinasG: Float,
    val carbosG: Float,
    val grasasG: Float
) {
    fun aReceta(): Receta = Receta(
        id = UUID.randomUUID().toString(),
        userId = "",
        titulo = nombre,
        kcalTotales = kcal,
        proteinasG = proteinasG,
        carbosG = carbosG,
        grasasG = grasasG,
        ingredientes = "Por 100 g · $tipo",
        fijada = false,
        creadaEn = null
    )
}

