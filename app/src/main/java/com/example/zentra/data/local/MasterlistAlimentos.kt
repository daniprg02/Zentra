/**
 * Base de datos local de alimentos comunes en España con sus macros por 100 g.
 * Se usa como fuente complementaria a la API de OpenFoodFacts, especialmente cuando
 * la conexión es lenta o los resultados de la API son escasos o en otro idioma.
